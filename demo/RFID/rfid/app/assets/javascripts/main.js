function GNRTReport() {
    var GNRTReport = document.getElementById("GNRTReport");
    //鼠标移入
    GNRTReport.onmouseover = function () {
        GNRTReport.style.backgroundColor = '#4F94CD';
    }
    //鼠标移出
    GNRTReport.onmouseout = function () {
        GNRTReport.style.backgroundColor = '#424A5D';
    }
}